public boolean chk() {
        if (ValiDate.isNull(txtMaTH, "Không được để trống Mã thương hiệu")) {
            return false;
        } else if (ValiDate.isNull(txtTenTH, "Không được để trống tên thương hiệu ")) {
            return false;
        }
         else {
            return true;
        }
    }
    public void ShowThuongHieu() {
        int i = tblThuongHieu.getSelectedRow();
        idWhenClick = tblThuongHieu.getValueAt(i, 1).toString();
        ThuongHieu_Model t = th.GetThuongHieuByMa(idWhenClick);
        txtMaTH.setText(t.getMa());
        txtTenTH.setText(t.getTen());
    }

    public void fillTableThuongHieu() {
        list = th.getAllThuongHieu();
        mol = (DefaultTableModel) tblThuongHieu.getModel();
        mol.setRowCount(0);
        for (ThuongHieu_View t : list) {
            mol.addRow(new Object[] { t.getStt(), t.getMa(), t.getTen() });
        }
    }
    public void fillTableThuongHieu(String key){
        list = th.Search(key);
        mol = (DefaultTableModel) tblThuongHieu.getModel();
        mol.setRowCount(0);
        for (ThuongHieu_View t : list) {
            mol.addRow(new Object[]{t.getStt(),t.getMa(),t.getTen()});
        }
    }
    public ThuongHieu_Model GetThuongHieu() {
        return new ThuongHieu_Model(txtMaTH.getText(), txtTenTH.getText());
    }


