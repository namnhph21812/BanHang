    private void btnThemTHActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnThemTHActionPerformed
        // TODO add your handling code here:
        
        ThuongHieu_Model th = GetThuongHieu();
        if (chk()) {
            int xacnhan = JOptionPane.showConfirmDialog(this, "Thêm Thương Hiệu" + txtMaTH.getText() + "?");
            if (xacnhan == JOptionPane.YES_OPTION) {
                this.th.Add(th);
                fillTableThuongHieu();
                JOptionPane.showMessageDialog(this, "Thêm Thương Hiệu Thành Công" + txtMaTH.getText());
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
                return;
            }
        }
    }// GEN-LAST:event_btnThemTHActionPerformed

    private void btnSuaTHActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSuaTHActionPerformed
        // TODO add your handling code here:
        ThuongHieu_Model th = GetThuongHieu();
        if (chk()) {
            int xacnhan = JOptionPane.showConfirmDialog(this, "Sủa Thương Hiệu" + txtMaTH.getText() + "?");
            if (xacnhan == JOptionPane.YES_OPTION) {
                this.th.Update(th);
                fillTableThuongHieu();
                JOptionPane.showMessageDialog(this, "Sủa Thương Hiệu Thành Công" + txtMaTH.getText());
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
                return;
            }
        }
    }// GEN-LAST:event_btnSuaTHActionPerformed
